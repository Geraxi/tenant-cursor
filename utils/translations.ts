export const t = (key: TranslationKey, params?: Record<string, string | number>): string => {
  let text = translations[key] || key;
  
  if (params) {
    Object.keys(params).forEach(paramKey => {
      text = text.replace(`{${paramKey}}`, String(params[paramKey]));
    });
  }
  
  return text;
};
